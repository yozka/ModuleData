 ///=====================================================================================
///
/// добавить данные в виджеты
/// 
/// 
///--------------------------------------------------------------------------------------
void AChart :: append(const double time, const double data)
{
	mContentTime.append(time);
	mContentData.append(data);

	
	if (mChartWidget != nullptr)
	{
		mChartWidget->append(time, data);
	}
}
